package genetic;

/**
 * Classe que representa uma fatia da roleta (pizza)
 *
 */

public class Fatia {
	private Double inicio;
	private Double fim;

	public Fatia() {

	}

	public Double getInicio() {
		return inicio;
	}

	public void setInicio(Double inicio) {
		this.inicio = inicio;
	}

	public Double getFim() {
		return fim;
	}

	public void setFim(Double fim) {
		this.fim = fim;
	}

}
